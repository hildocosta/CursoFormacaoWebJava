package cursojava.executavel;

public class Aula13 {
	
/* Título da Aula 13: Estrutura de Repetição For */

	
	public static void main(String[] args) {
		
	/*Estrutura de repetição For */
		
    /* numero ++: serve para quebrar a repetição, em ordem crescente */
	/* numero --: serve para quebrar a repetiçào, em ordem decrescente */
		
		for (int numero = 0; numero <= 10; numero ++ ) {
			System.out.println("O número atual é: " + numero);
		}
			
		
		}

}
