package cursojaca.executavel;

public class Aula11 {
	
	/* Título da Aula 11: Operadores Relacionais */
	
	/*
	 * 
	 *  == significa Igual
	 *  != significa Diferente
	 *  <  significa Menor
	 *  >  significa Maior
	 *  <= significa Menor Igual
	 *  >= significa Maior Igual
	 *  
	 *  
	 */
	

	public static void main(String[] args) {
		
		int nota1 = 91;
		int nota2 = 90;
		
		if (nota1 == nota2) {
			System.out.println(" As notas são iguais");
		}
		else {
			System.out.println(" As notas são diferentes");
		}


}
}