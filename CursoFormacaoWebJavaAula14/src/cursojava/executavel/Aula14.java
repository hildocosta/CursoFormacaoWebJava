package cursojava.executavel;

public class Aula14 {
	
/* Título da Aula 13: Estrutura de Repetição For e Parada com Break */

	
	public static void main(String[] args) {
		
		for (int numero = 0; numero <= 10; numero ++ ) {
			if (numero == 10) {
				System.out.println("Oba, encontrei o número 10");
				System.out.println("Estou parando de executar...");
				break;
			}
	}
		
	}
}
